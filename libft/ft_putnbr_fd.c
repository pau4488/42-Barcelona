#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
	long	nl;

	nl = n;
	if(nl < 0)
	{
		write(fd, "-", 1);
		nl = -nl;
	}
	if(nl >= 10)
	{
		ft_putnbr_fd(nl / 10, fd);
	}
	ft_putchar_fd("0123456789"[nl % 10], fd);
}